const mongoose = require("mongoose");

const loginSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    }
});

const Login = mongoose.model("bloglogin", loginSchema);

module.exports = Login;
